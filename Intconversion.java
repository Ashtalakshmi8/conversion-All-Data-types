class Intconversion {
	public static void main(String[]args){
		int i=98;
		byte s=(byte)i;//narrowing//
		System.out.println(s);
		
		
	short o=(short)i;//narrowing//
	System.out.println(s);
	
	int u=i;//widning//
	System.out.println(u);
	
	float f=i;//widning//
	System.out.println(f);
	
	long l=i;//widning//
	System.out.println(l);
	
	double d=i;//widning//
	System.out.println(d);
	//narrowing//
	char c=(char)i;
	System.out.println(c);//ascii no b//
	
	
	}
}


